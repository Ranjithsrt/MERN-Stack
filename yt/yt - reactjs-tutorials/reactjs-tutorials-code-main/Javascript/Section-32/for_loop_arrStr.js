//Array - For Loop

// To access each element of an array, we can use a for loop.

let arr = [10,20,30,40]

// console.log( arr[0] );

let length = arr.length

// console.log( length );

for( let i = 0; i<length; i++ ){
    console.log( arr[i] );
}

